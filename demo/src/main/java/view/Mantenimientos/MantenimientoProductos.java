    private void cargarCategoriasEnCombo() {
        CategoriaCombo.removeAllItems();
        List<Categoria> categorias = categoriaRepo.GetAll();
        for (Categoria categoria : categorias) {
            CategoriaCombo.addItem(categoria);
        }
        if (CategoriaCombo.getItemCount() > 0) {
            CategoriaCombo.setSelectedIndex(0);
        }
    }

    private Producto construirProductoParaInsertar() {
        Categoria categoria = (Categoria) CategoriaCombo.getSelectedItem();
        int categoriaId = categoria != null ? categoria.getID() : 0;
        return new Producto(0, NombreTxt.getText(), DescripcionTxt.getText(),
                parsePrecio(), categoriaId);
    }

    private Producto construirProductoDesdeSeleccion() {
        Integer id = obtenerIdSeleccionado();
        if (id == null) {
            return null;
        }
        Categoria categoria = (Categoria) CategoriaCombo.getSelectedItem();
        int categoriaId = categoria != null ? categoria.getID() : 0;
        return new Producto(id, NombreTxt.getText(), DescripcionTxt.getText(),
                parsePrecio(), categoriaId);
    }

    private double parsePrecio() {
        try {
            return Double.parseDouble(PrecioTxt.getText());
        } catch (NumberFormatException e) {
            return 0d;
        }
    }

    private Integer obtenerIdSeleccionado() {
        int fila = jTable1.getSelectedRow();
        if (fila < 0) {
            return null;
        }
        Object valor = jTable1.getValueAt(fila, 0);
        if (valor instanceof Integer) {
            return (Integer) valor;
        }
        if (valor instanceof String) {
            try {
                return Integer.parseInt((String) valor);
            } catch (NumberFormatException ex) {
                return null;
            }
        }
        return null;
    }

    private void rellenarCampos(Producto producto) {
        NombreTxt.setText(producto.getNombre());
        DescripcionTxt.setText(producto.getDescripcion());
        PrecioTxt.setText(String.valueOf(producto.getPrecio()));
        seleccionarCategoriaEnCombo(producto.getCategoria_ID());
    }

    private void seleccionarCategoriaEnCombo(int categoriaId) {
        for (int i = 0; i < CategoriaCombo.getItemCount(); i++) {
            Categoria cat = CategoriaCombo.getItemAt(i);
            if (cat != null && cat.getID() == categoriaId) {
                CategoriaCombo.setSelectedIndex(i);
                return;
            }
        }
    }

    private void seleccionarEnTablaPorId(int id) {
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            Object valor = jTable1.getValueAt(i, 0);
            if (valor instanceof Integer && ((Integer) valor) == id) {
                jTable1.setRowSelectionInterval(i, i);
                return;
            }
            if (valor instanceof String) {
                try {
                    if (Integer.parseInt((String) valor) == id) {
                        jTable1.setRowSelectionInterval(i, i);
                        return;
                    }
                } catch (NumberFormatException ignored) {
                }
            }
        }
    }
