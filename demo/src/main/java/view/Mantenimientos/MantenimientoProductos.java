    private void BotonInsertarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BotonInsertarActionPerformed

        Producto nuevo = construirProductoParaInsertar();
        if (nuevo == null) {
            return;
        }
        ProductoRep.Insert(nuevo);
        cargarTabla();
    }// GEN-LAST:event_BotonInsertarActionPerformed

    private void BotonModificarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BotonModificarActionPerformed
        Producto actualizado = construirProductoDesdeSeleccion();
        if (actualizado == null) {
            return;
        }
        ProductoRep.Update(actualizado);
        cargarTabla();
    }// GEN-LAST:event_BotonModificarActionPerformed

    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BotonEliminarActionPerformed
        Integer id = obtenerIdSeleccionado();
        if (id == null) {
            return;
        }
        ProductoRep.Delete(id);
        cargarTabla();
    }// GEN-LAST:event_BotonEliminarActionPerformed

    private void BotonActualizarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BotonActualizarActionPerformed
        cargarTabla();
    }// GEN-LAST:event_BotonActualizarActionPerformed

    private void BotonBuscarIdActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BotonBuscarIdActionPerformed
        String idTexto = Buscarprecio.getText();
        if (idTexto == null || idTexto.isBlank()) {
            return;
        }
        Integer id;
        try {
            id = Integer.parseInt(idTexto);
        } catch (NumberFormatException ex) {
            return;
        }
        Producto encontrado = ProductoRep.GetById(id);
        if (encontrado != null) {
            rellenarCampos(encontrado);
            seleccionarEnTablaPorId(encontrado.getID());
        }
    }// GEN-LAST:event_BotonBuscarIdActionPerformed

    private void BotonAgregarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {
        String nombre = NuevaCategoriaTxt.getText();
        if (nombre == null || nombre.isBlank()) {
            return;
        }
        int nuevoId = categoriaRepo.GetAll().size() + 1;
        Categoria nueva = new Categoria(nuevoId, nombre);
        categoriaRepo.Insert(nueva);
        cargarCategoriasEnCombo();
        seleccionarCategoriaEnCombo(nuevoId);
        NuevaCategoriaTxt.setText("");
    }

